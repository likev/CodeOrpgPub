  fprintf(stderr," \n");
  fprintf(stderr,"==============================================================================\n");
  fprintf(stderr," \n");
  fprintf(stderr,"- Product Extraction Commands\n");  
  fprintf(stderr,"  ---------------------------\n");
  fprintf(stderr," \n");
  fprintf(stderr," CVT can extract ICD formatted final product messages from the product database\n");
  fprintf(stderr," linear buffer. In addition, CVT can extract intermediate product messages from\n");
