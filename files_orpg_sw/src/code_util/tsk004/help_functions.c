  fprintf(stderr,"                                  parameters\n");
  fprintf(stderr,"generic print X               Print the component with the index 'X' in the\n");
  fprintf(stderr,"                                  component listing\n");
  fprintf(stderr,"generic print_area            Print all area components in the generic product\n");
  fprintf(stderr,"generic print_area no_cparams Print all area components, omit area component\n");
