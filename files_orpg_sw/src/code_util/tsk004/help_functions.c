  fprintf(stderr,"  print X\n");
  fprintf(stderr," \n");
  fprintf(stderr," By default, all components are printed unless a component print selector or\n");
  fprintf(stderr," component list selector is used.  The print selector has two forms.  The first\n");
  fprintf(stderr," causes all components of a particular type to be displayed.  The second form \n");
  fprintf(stderr," 'print X' causes the component with an index 'X' to be printed.\n");
