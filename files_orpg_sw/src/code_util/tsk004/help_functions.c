  fprintf(stderr,"                 no_pparams | no_cparams | no_pparams no_cparams \n");
  fprintf(stderr," \n");
  fprintf(stderr," A The parameter print selector controls whether the product parameters and \n");
  fprintf(stderr," parameters associated with individual components are printed.  The default is \n");
  fprintf(stderr," all parameters printed.  Both values can be used together to eliminate the \n");
  fprintf(stderr," printing of all parameters.\n");
  fprintf(stderr," \n");

