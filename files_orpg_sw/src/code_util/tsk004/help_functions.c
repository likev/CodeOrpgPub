  fprintf(stderr," \n");
  fprintf(stderr," \n");
  fprintf(stderr,"- Generic Product Modifier List Structure\n");
  fprintf(stderr,"  -----------------------\n");
  fprintf(stderr," A Generic Product Modifier list consists of optional selectors: <component list>\n");
  fprintf(stderr," selector, <component print> selector, <parameter print> selector, and <decode \n");
  fprintf(stderr," selector>. These can be used in any order though use of a <component list> \n");
  fprintf(stderr," selector will render the <component print> selector non-functional.  \n");
  fprintf(stderr," The possible values for the selectors are: \n");
  fprintf(stderr," \n");
  fprintf(stderr,"                               [<component list>]\n");
