  fprintf(stderr,"generic print_radial          Print all radial components \n");
  fprintf(stderr,"generic print_radial radial A Print all radial components but only print data\n");
  fprintf(stderr,"                                  for radial A\n");
  fprintf(stderr,"generic radial A deg          Print entire generic product except only print\n");
  fprintf(stderr,"                                  data for radial A degrees in radial component\n");
  fprintf(stderr,"generic radial A B            Print entire generic product except only print\n");
  fprintf(stderr,"                                  data from radial A to radial B inclusive\n");
  fprintf(stderr," \n");
  fprintf(stderr," For generic products containing radial components, the modifier 'generic' can\n");
  fprintf(stderr," be omitted if the 'radial' modifier is used. However without the 'generic'  \n");
  fprintf(stderr," modifier the [<component list>], [<component print>], and <parameter print>] \n");
