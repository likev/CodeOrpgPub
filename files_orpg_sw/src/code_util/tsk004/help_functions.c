  fprintf(stderr," list selector disables the component print selectors. \n");
  fprintf(stderr," \n");
  fprintf(stderr,"                               [<component print>]\n");
  fprintf(stderr,"  ----------------------------------------------------------------------------\n");
  fprintf(stderr,"  print_area | print_rad | print_text | print_table | print_grid | print_event\n");
